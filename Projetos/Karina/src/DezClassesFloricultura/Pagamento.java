package DezClassesFloricultura;

public class Pagamento {
	private Integer dinheiro;
	private Integer cartao;
	private Integer cheque;
	private Integer dividir;
	
	public Integer getDinheiro() {
		return dinheiro;
	}
	public void setDinheiro(Integer dinheiro) {
		this.dinheiro = dinheiro;
	}
	public Integer getCartao() {
		return cartao;
	}
	public void setCartao(Integer cartao) {
		this.cartao = cartao;
	}
	public Integer getCheque() {
		return cheque;
	}
	public void setCheque(Integer cheque) {
		this.cheque = cheque;
	}
	public Integer getDividir() {
		return dividir;
	}
	public void setDividir(Integer dividir) {
		this.dividir = dividir;
	}
	

}
