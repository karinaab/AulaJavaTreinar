package DezClassesFloricultura;

import java.util.List;

public class Utensilio {
	private List<String> ferramentas;
	private List<String> fertilizantes;
	private List<String> inseticidas;
	
	public List<String> getFerramentas() {
		return ferramentas;
	}
	public void setFerramentas(List<String> ferramentas) {
		this.ferramentas = ferramentas;
	}
	public List<String> getFertilizantes() {
		return fertilizantes;
	}
	public void setFertilizantes(List<String> fertilizantes) {
		this.fertilizantes = fertilizantes;
	}
	public List<String> getInseticidas() {
		return inseticidas;
	}
	public void setInseticidas(List<String> inseticidas) {
		this.inseticidas = inseticidas;
	}

}

